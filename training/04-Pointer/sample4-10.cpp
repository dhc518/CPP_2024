#include <iostream>

class Circle {
	int radius;
public:
	Circle();
	~Circle(){}
	void setRadius(int r) { radius = r; }	
	double getArea() { return 3.14 * radius * radius; }

};

Circle::Circle(){
	radius = 1;
}

int main() {
	std::cout << "생성하고자 하는 원의 개수?";
	int n, radius;
	std::cin >> n;
	

	Circle* pArray = new Circle[n];
	for (int i = 0; i < n; i++) {
		std::cout << "원" << i+1 << ": ";
		std::cin >> radius;
		pArray[i].setRadius(radius);
	}

	int count = 0;
	for (int i = 0; i < n; i++) {
		std::cout << pArray[i].getArea() << " ";
		if (pArray[i].getArea() > 100 && pArray[i].getArea() < 200) count++;
	}

	std::cout << std::endl
		<<"면적이 100에서 200 사이인 원의 개수는 " << count;

	delete[] pArray;

}