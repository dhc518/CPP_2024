#include <iostream>
#include <string>

int main() {
	// 스트링 생성
	std::string str; // 빈 문자열을 가진 스트링 객체 생성
	std::string address("서울시 성북구 삼선동 389");
	std::string copyAddress(address); // address의 문자열을 복사한 스트링 객체 생성
	char text[] = { 'L', 'o', 'v', 'e', ' ', 'C', '+', '+', '\0' }; // C-스트링
	std::string title(text); // "Love C++" 문자열을 가진 스트링 객체 생성
	// 스트링 출력
	std::cout << str << std::endl; // 빈 스트링. 아무 값도 출력되지 않음
	std::cout << address << std::endl;
	std::cout << copyAddress << std::endl;
	std::cout << title << std::endl;
}